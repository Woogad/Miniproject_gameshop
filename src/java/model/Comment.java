package model;


public class Comment {
    
    String Comment_User;
    String Comment_HeadText;
    String Comment_CommentText;
    String Comment_Date;

    public String getComment_User() {
        return Comment_User;
    }

    public void setComment_User(String Comment_User) {
        this.Comment_User = Comment_User;
    }

    public String getComment_HeadText() {
        return Comment_HeadText;
    }

    public void setComment_HeadText(String Comment_HeadText) {
        this.Comment_HeadText = Comment_HeadText;
    }

    public String getComment_CommentText() {
        return Comment_CommentText;
    }

    public void setComment_CommentText(String Comment_CommentText) {
        this.Comment_CommentText = Comment_CommentText;
    }

    public String getComment_Date() {
        return Comment_Date;
    }

    public void setComment_Date(String Comment_Date) {
        this.Comment_Date = Comment_Date;
    }
    
}
